#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*sub;
	size_t	len_s;

	if (!s)
		return (NULL);
	len_s = ft_strlen(s);
	if (start >= len_s)
		return (ft_strdup(""));
	if (len > len_s - start)
		len = len_s - start;
	sub = (char *)malloc(len + 1);
	if (!sub)
		return (NULL);
	sub = ft_memcpy(sub, &s[start], len);
	sub[len] = '\0';
	return (sub);
}
